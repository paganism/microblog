@bp.route('/edit_profile', methods=['GET', 'POST'])
@login_required
def edit_profile():
    user = User.query.filter_by(username=current_user.username).first_or_404()
    form = EditProfileForm(current_user.username)
    filepath = user.photo_path
    if form.validate_on_submit():
        photo = form.photo.data
        filename = secure_filename(photo.filename)
        user_dir = os.path.join(current_app.config['UPLOAD_FOLDER'], str(user.id))
        if not os.path.exists(user_dir):
            os.mkdir(user_dir)
        photo.save(os.path.join(user_dir, filename))
        image_dir = os.path.join(user_dir, filename)
        image = get_image(image_dir)
        old_dimensions = image.size
        new_width, new_height, ratio, new_ratio = get_new_dimensions(
        old_dimensions, 128)
        resized_image = resize_image(image, new_width, new_height, image_dir)
        filepath = os.path.join(current_app.config['AVATAR_FOLDER'], str(user.id), filename)
        current_user.username = form.username.data
        current_user.about_me = form.about_me.data
        current_user.set_avatar_path(filepath)
        db.session.commit()
        flash('Your changes have been saved')
        return redirect(url_for('main.edit_profile'))
    elif request.method == 'GET':
        form.username.data = current_user.username
        form.about_me.data = current_user.about_me
        form.photo.data = current_user.photo_path
    if not filepath:
        filepath = '/static/avatars/muay.jpg'
    return render_template('edit_profile.html', title=_('Edit Profile'), form=form, user=user, filepath=filepath)


@bp.route('/follow/<username>')
@login_required
def follow(username):
    user = User.query.filter_by(username=username).first()
    if user is None:
        flash('User {} not found.'.format(username))
        return redirect(url_for('main.index'))
    if user == current_user:
        flash('You cannot follow yourself!')
        return redirect(url_for('main.user', username=username))
    current_user.follow(user)
    db.session.commit()
    flash('You are following {}!'.format(username))
    return redirect(url_for('main.user', username=username))


